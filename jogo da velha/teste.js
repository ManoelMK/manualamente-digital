document.querySelector('.d1').addEventListener('click',()=>{
    let d1 = document.querySelector('.d1');

    if (d1.classList.contains('d10')) {
        d1.classList.add('d11')
        d1.classList.remove('d10')
    } else {
        d1.classList.add('d10')
        d1.classList.remove('d11')
    }

})
